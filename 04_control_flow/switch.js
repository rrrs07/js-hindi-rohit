const month = 3

switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("February");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;

    default:
        console.log("default case");
        break;
}

//(so if the condition which you are looking for is met then the break not execute the restt of code except the `Default case` which will get printed no matter what)